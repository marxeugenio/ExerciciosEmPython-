import numpy as np

# Cria um array de exemplo 
a = np.array([1,2,3,4,5])

# Funções matemáticas

b = np.sin(a)
c = np.log(a)
d = np.exp(a)

# Funções estatísticas
e = np.mean(a)
f = np.std(a)
g = np.min(a)
h = np.max(a)


print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)

